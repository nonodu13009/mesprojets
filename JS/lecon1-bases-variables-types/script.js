// Soustraction (-) : 10 - 20 = -10
console.log(nombre - nombre2);

// Multiplication (*) : 10 × 20 = 200
console.log(nombre * nombre2);

// Division (/) : 10 ÷ 20 = 0.5
console.log(nombre / nombre2);

// Modulo (%) : reste de la division, 10 ÷ 20 → reste 10
console.log(nombre % nombre2);

// Puissance (**) : 10^20
console.log(nombre ** nombre2);

// Égalité (==) : 10 est-il égal à 20 ? → false
console.log(nombre == nombre2);

// Différent (!==) : 10 est-il différent de 20 ? → true
console.log(nombre !== nombre2);

